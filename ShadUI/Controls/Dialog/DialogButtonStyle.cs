// ReSharper disable once CheckNamespace
namespace ShadUI;

/// <summary>
///     Defines the style of the button in the simple dialog.
/// </summary>
public enum DialogButtonStyle
{
    /// <summary>
    ///     Sets the button style class as Primary.
    /// </summary>
    Primary,

    /// <summary>
    ///     Sets the button style class as Secondary.
    /// </summary>
    Secondary,

    /// <summary>
    ///     Sets the button style class as Outline.
    /// </summary>
    Outline,

    /// <summary>
    ///     Sets the button style class as Ghost.
    /// </summary>
    Ghost,

    /// <summary>
    ///     Sets the button style class as Destruct
    /// </summary>
    Destructive
}