namespace ShadUI;

/// <summary>
///     Defines the position of the toast.
/// </summary>
public enum ToastPosition
{
    /// <summary>
    ///     Places the toast in the bottom right corner of the screen.
    /// </summary>
    BottomRight,

    /// <summary>
    ///     Places the toast in the bottom left corner of the screen.
    /// </summary>
    BottomLeft,

    /// <summary>
    ///     Places the toast in the bottom center corner of the screen.
    /// </summary>
    BottomCenter,

    /// <summary>
    ///     Places the toast in the top right corner of the screen.
    /// </summary>
    TopRight,

    /// <summary>
    ///     Places the toast in the top left corner of the screen.
    /// </summary>
    TopLeft,

    /// <summary>
    ///     Places the toast in the top center corner of the screen.
    /// </summary>
    TopCenter
}